/**
 * 年・月から節入り日（その月の節気が始まる日）を返す。
 * PDF テーブルにある年は厳密値、無い年は固定テーブル SETSU_DAYS にフォールバック。
 *
 * @param year 西暦の年
 * @param month 1〜12 の月
 * @param fallback フォールバックとして使う既存の固定値（{1: 6, 2: 4, ..., 12: 7}）
 */
export function getSetsuDay(
  year: number,
  month: number,
  fallback: Record<number, number>,
): number {
  // 1月の小寒は、PDF テーブル上「前年の index 11」に格納されている
  if (month === 1) {
    const prev = SETSU_DAYS_BY_YEAR[year - 1];
    if (prev) return prev[11];
    return fallback[1] ?? 6;
  }
  const arr = SETSU_DAYS_BY_YEAR[year];
  if (arr) return arr[month - 2];
  return fallback[month] ?? 6;
}
